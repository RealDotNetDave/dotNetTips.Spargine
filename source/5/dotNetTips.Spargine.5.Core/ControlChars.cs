namespace dotNetTips.Spargine.Core
{
	/// <summary>
	/// Class ControlChars.
	/// </summary>
	public static class ControlChars
	{
		/// <summary>
		/// @ Symbol.
		/// </summary>
		public const char At = '@';

		/// <summary>
		/// Back Space.
		/// </summary>
		public const char Back = '\b';

		/// <summary>
		/// Back Slash.
		/// </summary>
		public const char Backslash = '\\';

		/// <summary>
		/// The back tick.
		/// </summary>
		public const char BackTick = '`';

		/// <summary>
		/// Colon.
		/// </summary>
		public const char Colon = ':';

		/// <summary>
		/// Comma.
		/// </summary>
		public const char Comma = ',';

		/// <summary>
		/// Carriage Return.
		/// </summary>
		public const char CR = '\r';

		/// <summary>
		/// Carriage Return &amp; Line Feed.
		/// </summary>
		public const string CRLF = "\r\n";

		/// <summary>
		/// The default separator
		/// </summary>
		public const string DefaultSeparator = ", ";

		/// <summary>
		/// Dot (period).
		/// </summary>
		public const char Dot = '.';

		/// <summary>
		/// Double quote.
		/// </summary>
		public const string DoubleQuote = "''";

		/// <summary>
		/// Empty String
		/// </summary>
		public const string EmptyString = "";

		/// <summary>
		/// The end angle bracket.
		/// </summary>
		public const char EndAngleBracket = '>';

		/// <summary>
		/// ).
		/// </summary>
		public const char EndComment = ')';

		/// <summary>
		/// [.
		/// </summary>
		public const char EndSquareBracket = ']';

		/// <summary>
		/// The equal symbol.
		/// </summary>
		public const char Equal = '=';

		/// <summary>
		/// Form feed.
		/// </summary>
		public const char FormFeed = '\f';

		/// <summary>
		/// Forward slash.
		/// </summary>
		public const char ForwardSlash = '/';

		/// <summary>
		/// The Line Feed.
		/// </summary>
		public const char LF = '\n';

		/// <summary>
		/// New line.
		/// </summary>
		public const char NewLine = '\n';

		/// <summary>
		/// Null character.
		/// </summary>
		public const char NullChar = '\0';

		/// <summary>
		/// The plus character
		/// </summary>
		public const char Plus = '+';

		/// <summary>
		/// Quote.
		/// </summary>
		public const char Quote = '\"';

		/// <summary>
		/// Single quote.
		/// </summary>
		public const char SingleQuote = '\'';

		/// <summary>
		/// Space.
		/// </summary>
		public const char Space = ' ';

		/// <summary>
		/// Left angle bracket.
		/// </summary>
		public const char StartAngleBracket = '<';

		/// <summary>
		/// (.
		/// </summary>
		public const char StartComment = '(';

		/// <summary>
		/// [.
		/// </summary>
		public const char StartSquareBracket = '[';

		/// <summary>
		/// Tab.
		/// </summary>
		public const char Tab = '\t';

		/// <summary>
		/// Underscore.
		/// </summary>
		public const char Underscore = '_';

		/// <summary>
		/// Vertical tab.
		/// </summary>
		public const char VerticalTab = '\v';
	}
}
